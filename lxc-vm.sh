# Function to check if running
is_vm_running() {
    local vm_name=$1
    if lxc info "$vm_name" 2>/dev/null | grep -q "Status: RUNNING"; then
        return 0
    else
        return 1
    fi
}

# Function to edit configuration
edit_vm_config() {
    local vm_name=$1
    
    if load_vm_config "$vm_name"; then
        print_status "INFO" "Modifying resource allocation matrices: $vm_name"
        
        while true; do
            echo "What would you like to edit?"
            echo "  1) Hostname"
            echo "  2) Username"
            echo "  3) Password"
            echo "  4) SSH Port mapping"
            echo "  5) Port Forwards configuration"
            echo "  6) Memory Allocation (RAM)"
            echo "  7) CPU Core Count"
            echo "  8) Disk Space Scale"
            echo "  0) Return to Dashboard"
            
            read -p "$(print_status "INPUT" "Enter your choice: ")" edit_choice
            
            case $edit_choice in
                1)
                    read -p "$(print_status "INPUT" "Enter new hostname (current: $HOSTNAME): ")" new_hostname
                    HOSTNAME="${new_hostname:-$HOSTNAME}"
                    ;;
                2)
                    read -p "$(print_status "INPUT" "Enter new username (current: $USERNAME): ")" new_username
                    USERNAME="${new_username:-$USERNAME}"
                    ;;
                3)
                    read -p "$(print_status "INPUT" "Enter new password: ")" new_password
                    PASSWORD="${new_password:-$PASSWORD}"
                    ;;
                4)
                    read -p "$(print_status "INPUT" "Enter new SSH mapping port (current: $SSH_PORT): ")" new_ssh_port
                    SSH_PORT="${new_ssh_port:-$SSH_PORT}"
                    ;;
                5)
                    read -p "$(print_status "INPUT" "Port Forwards string (current: ${PORT_FORWARDS:-None}): ")" new_port_forwards
                    PORT_FORWARDS="${new_port_forwards:-$PORT_FORWARDS}"
                    ;;
                6)
                    read -p "$(print_status "INPUT" "Enter Memory Size in MB (current: $MEMORY): ")" new_memory
                    MEMORY="${new_memory:-$MEMORY}"
                    ;;
                7)
                    read -p "$(print_status "INPUT" "Enter CPU Core count (current: $CPUS): ")" new_cpus
                    CPUS="${new_cpus:-$CPUS}"
                    ;;
                8)
                    read -p "$(print_status "INPUT" "Enter Extended Disk Size (current: $DISK_SIZE): ")" new_disk_size
                    DISK_SIZE="${new_disk_size:-$DISK_SIZE}"
                    ;;
                0) return 0 ;;
                *) print_status "ERROR" "Invalid option selection"; continue ;;
            esac
            
            setup_vm_image
            save_vm_config
            
            read -p "$(print_status "INPUT" "Continue adjusting variables? (y/N): ")" continue_editing
            if [[ ! "$continue_editing" =~ ^[Yy]$ ]]; then
                break
            fi
        done
    fi
}

# Function to resize disk allocation
resize_vm_disk() {
    local vm_name=$1
    if load_vm_config "$vm_name"; then
        print_status "INFO" "Current disk footprint configuration: $DISK_SIZE"
        while true; do
            read -p "$(print_status "INPUT" "Enter new destination boundary target format size (e.g., 50G): ")" new_disk_size
            if validate_input "size" "$new_disk_size"; then
                local formatted_size="$new_disk_size"
                if [[ "$formatted_size" =~ [Gg]$ ]]; then
                    formatted_size="${formatted_size%[Gg]}GiB"
                elif [[ "$formatted_size" =~ [Mm]$ ]]; then
                    formatted_size="${formatted_size%[Mm]}MiB"
                fi

                print_status "INFO" "Altering device configuration boundaries inside storage structures..."
                if lxc config device set "$vm_name" root size="$formatted_size" 2>/dev/null || lxc config device override "$vm_name" root size="$formatted_size" 2>/dev/null; then
                    DISK_SIZE="$new_disk_size"
                    save_vm_config
                    print_status "SUCCESS" "Storage parameters extended successfully to $new_disk_size."
                else
                    print_status "WARN" "LXC Storage Driver adjustments logged. Driver constraints may block runtime changes."
                fi
                break
            fi
        done
    fi
}

# Function to show live metrics
show_vm_performance() {
    local vm_name=$1
    if load_vm_config "$vm_name"; then
        if is_vm_running "$vm_name"; then
            print_status "INFO" "Exposing Active Runtime Telemetry: $vm_name"
            echo "=========================================================="
            lxc info "$vm_name"
            echo "=========================================================="
        else
            print_status "INFO" "Target Profile instance structural loop $vm_name is not active."
        fi
        read -p "$(print_status "INPUT" "Press Enter to continue...")"
    fi
}

# Main dashboard interface
main_menu() {
    while true; do
        display_header
        echo -e "Host Capabilities Target Mode: \033[1;35m$HOST_MODE_LABEL\033[0m"
        echo "------------------------------------------------------------------------"
        
        local vms=($(get_vm_list))
        local vm_count=${#vms[@]}
        
        if [ $vm_count -gt 0 ]; then
            print_status "INFO" "Detected $vm_count Managed Environment Profiles:"
            for i in "${!vms[@]}"; do
                local status="Stopped"
                if is_vm_running "${vms[$i]}"; then
                    status="Running"
                fi
                # Load profile momentarily to identify its execution nature (VM vs Container)
                if load_vm_config "${vms[$i]}" &>/dev/null; then
                    printf "  %2d) %s [%s] (%s)\n" $((i+1)) "${vms[$i]}" "$VM_MODE" "$status"
                else
                    printf "  %2d) %s (%s)\n" $((i+1)) "${vms[$i]}" "$status"
                fi
            done
            echo
        fi
        
        echo "Main Control Dashboard Operations:"
        echo "  1) Create a new Instance Profile"
        if [ $vm_count -gt 0 ]; then
            echo "  2) Boot/Start Target Environment Instance"
            echo "  3) Shut down/Stop Instance Target Loop"
            echo "  4) Inspect Profile System configurations"
            echo "  5) Modify Resource Parameters & Limits"
            echo "  6) Destroy/Erase Instance Profile Data"
            echo "  7) Adjust Disk/Storage Volumes Scale"
            echo "  8) Read Metric Diagnostics Data"
        fi
        echo "  0) Exit Script Engine"
        echo
        
        read -p "$(print_status "INPUT" "Enter targeted action menu key: ")" choice
        
        case $choice in
            1) create_new_vm ;;
            2)
                if [ $vm_count -gt 0 ]; then
                    read -p "$(print_status "INPUT" "Start Instance Index: ")" vm_num
                    if [[ "$vm_num" =~ ^[0-9]+$ ]] && [ "$vm_num" -ge 1 ] && [ "$vm_num" -le $vm_count ]; then
                        start_vm "${vms[$((vm_num-1))]}"
                    else
                        print_status "ERROR" "Index range configuration argument invalid."
                    fi
                fi
                ;;
            3)
                if [ $vm_count -gt 0 ]; then
                    read -p "$(print_status "INPUT" "Terminate Instance Index: ")" vm_num
                    if [[ "$vm_num" =~ ^[0-9]+$ ]] && [ "$vm_num" -ge 1 ] && [ "$vm_num" -le $vm_count ]; then
                        stop_vm "${vms[$((vm_num-1))]}"
                    else
                        print_status "ERROR" "Index range configuration argument invalid."
                    fi
                fi
                ;;
            4)
                if [ $vm_count -gt 0 ]; then
                    read -p "$(print_status "INPUT" "Inspect Profile Index: ")" vm_num
                    if [[ "$vm_num" =~ ^[0-9]+$ ]] && [ "$vm_num" -ge 1 ] && [ "$vm_num" -le $vm_count ]; then
                        show_vm_info "${vms[$((vm_num-1))]}"
                    else
                        print_status "ERROR" "Index range configuration argument invalid."
                    fi
                fi
                ;;
            5)
                if [ $vm_count -gt 0 ]; then
                    read -p "$(print_status "INPUT" "Modify Parameters Index: ")" vm_num
                    if [[ "$vm_num" =~ ^[0-9]+$ ]] && [ "$vm_num" -ge 1 ] && [ "$vm_num" -le $vm_count ]; then
                        edit_vm_config "${vms[$((vm_num-1))]}"
                    else
                        print_status "ERROR" "Index range configuration argument invalid."
                    fi
                fi
                ;;
            6)
                if [ $vm_count -gt 0 ]; then
                    read -p "$(print_status "INPUT" "Destroy Target Index: ")" vm_num
                    if [[ "$vm_num" =~ ^[0-9]+$ ]] && [ "$vm_num" -ge 1 ] && [ "$vm_num" -le $vm_count ]; then
                        delete_vm "${vms[$((vm_num-1))]}"
                    else
                        print_status "ERROR" "Index range configuration argument invalid."
                    fi
                fi
                ;;
            7)
                if [ $vm_count -gt 0 ]; then
                    read -p "$(print_status "INPUT" "Target Storage Dimension Index: ")" vm_num
                    if [[ "$vm_num" =~ ^[0-9]+$ ]] && [ "$vm_num" -ge 1 ] && [ "$vm_num" -le $vm_count ]; then
                        resize_vm_disk "${vms[$((vm_num-1))]}"
                    else
                        print_status "ERROR" "Index range configuration argument invalid."
                    fi
                fi
                ;;
            8)
                if [ $vm_count -gt 0 ]; then
                    read -p "$(print_status "INPUT" "Target Metric Diagnostics Index: ")" vm_num
                    if [[ "$vm_num" =~ ^[0-9]+$ ]] && [ "$vm_num" -ge 1 ] && [ "$vm_num" -le $vm_count ]; then
                        show_vm_performance "${vms[$((vm_num-1))]}"
                    else
                        print_status "ERROR" "Index range configuration argument invalid."
                    fi
                fi
                ;;
            0) print_status "INFO" "Exiting Script Engine."; exit 0 ;;
            *) print_status "ERROR" "Selection argument execution mismatch error." ;;
        esac
        
        read -p "$(print_status "INPUT" "Press Enter to return to Dashboard...")"
    done
}

